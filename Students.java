package asb_system;

public class Students {
	
	private String student_one_name;
	private String student_two_name;
	private String student_three_name;
	public Students(String student_one_name, String student_two_name, String student_three_name) {
		
		this.student_one_name = student_one_name;
		this.student_two_name = student_two_name;
		this.student_three_name = student_three_name;
		
	}
	public Students() {
			}
	public String getStudent_one_name() {
		return student_one_name;
	}
	public void setStudent_one_name(String student_one_name) {
		this.student_one_name = student_one_name;
	}
	public String getStudent_two_name() {
		return student_two_name;
	}
	public void setStudent_two_name(String student_two_name) {
		this.student_two_name = student_two_name;
	}
	public String getStudent_three_name() {
		return student_three_name;
	}
	public void setStudent_three_name(String student_three_name) {
		this.student_three_name = student_three_name;
	}
	
	
	
	
	
		
	
	
	
}
